#!/usr/bin/env node

// Package the static demo as a single-file executable.
//
//   node scripts/package-demo-bun.mjs [--target bun-windows-x64|bun-linux-x64|bun-darwin-arm64|...]
//
// Rebuilds dist/static-publish (the protected artifact), generates a tiny
// Bun.serve entry that embeds every allowlisted file, and runs
// `bun build --compile` to produce dist/release/projects-demo[.exe].
// Requires Bun >= 1.3 on PATH at package time only; the produced binary is
// self-contained. The binary is a release artifact — distribute via GitHub
// Releases, never commit it (dist/ is gitignored and the public-asset gates
// would reject it).

import { spawnSync } from "node:child_process";
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { STATIC_PUBLISH_FILES, buildStaticPublish, DEFAULT_STATIC_PUBLISH_DIR } from "./build-static-publish.mjs";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const entryDir = path.join(repoRoot, "dist", "demo-exe");
const releaseDir = path.join(repoRoot, "dist", "release");
const targetFlagIndex = process.argv.indexOf("--target");
const target = targetFlagIndex !== -1 ? process.argv[targetFlagIndex + 1] : "";
const outfileBase = path.join(releaseDir, "projects-demo");

const bunVersion = runBun(["--version"], { capture: true });
if (bunVersion.status !== 0) {
  throw new Error("Bun is required on PATH to package the demo executable (https://bun.com). Install it, then re-run.");
}
console.log(`Packaging with bun ${String(bunVersion.stdout).trim()}`);

await buildStaticPublish(DEFAULT_STATIC_PUBLISH_DIR);
console.log("Static artifact rebuilt: dist/static-publish");

const mime = {
  ".html": "text/html; charset=utf-8",
  ".js": "text/javascript; charset=utf-8",
  ".css": "text/css; charset=utf-8",
  ".json": "application/json; charset=utf-8",
  ".png": "image/png",
  ".svg": "image/svg+xml"
};

const lines = [];
const routes = [];
STATIC_PUBLISH_FILES.forEach((relativeFile, index) => {
  const webPath = `/${relativeFile.replaceAll("\\", "/")}`;
  const importPath = `../static-publish/${relativeFile.replaceAll("\\", "/")}`;
  lines.push(`import f${index} from ${JSON.stringify(importPath)} with { type: "file" };`);
  const ext = path.extname(relativeFile);
  routes.push(`  [${JSON.stringify(webPath)}, { file: f${index}, type: ${JSON.stringify(mime[ext] || "application/octet-stream")} }],`);
});

const entrySource = `// generated by scripts/package-demo-bun.mjs — do not edit
${lines.join("\n")}

const routes = new Map([
${routes.join("\n")}
]);

const server = Bun.serve({
  port: Number(process.env.PROJECTS_DEMO_PORT || 0),
  fetch(request) {
    let pathname = decodeURIComponent(new URL(request.url).pathname);
    if (pathname === "/") pathname = "/index.html";
    const route = routes.get(pathname);
    if (!route) return new Response("not found", { status: 404 });
    return new Response(Bun.file(route.file), { headers: { "content-type": route.type } });
  }
});

const url = \`http://127.0.0.1:\${server.port}\`;
console.log(\`Projects demo running at \${url} — press Ctrl+C to stop.\`);
if (!process.argv.includes("--no-open")) {
  const opener = process.platform === "win32"
    ? ["cmd", "/c", "start", "", url]
    : process.platform === "darwin" ? ["open", url] : ["xdg-open", url];
  try { Bun.spawn(opener, { stdout: "ignore", stderr: "ignore" }); } catch {}
}
`;

await fs.rm(entryDir, { recursive: true, force: true });
await fs.mkdir(entryDir, { recursive: true });
await fs.mkdir(releaseDir, { recursive: true });
const entryPath = path.join(entryDir, "entry.js");
await fs.writeFile(entryPath, entrySource, "utf8");

const args = ["build", "--compile", "--minify", entryPath, "--outfile", outfileBase];
if (target) args.push("--target", target);
const build = runBun(args);
if (build.status !== 0) {
  throw new Error("bun build --compile failed.");
}

const produced = (await fs.readdir(releaseDir)).filter((name) => name.startsWith("projects-demo"));
for (const name of produced) {
  const size = (await fs.stat(path.join(releaseDir, name))).size;
  console.log(`Built dist/release/${name} (${Math.round(size / 1048576)} MB)`);
}
console.log("Run it directly; it serves the demo locally and opens your browser (pass --no-open to suppress).");

function runBun(bunArgs, options = {}) {
  return spawnSync("bun", bunArgs, {
    cwd: repoRoot,
    encoding: "utf8",
    stdio: options.capture ? "pipe" : "inherit",
    // npm-installed bun is bun.cmd on Windows; .cmd needs a shell to launch.
    shell: process.platform === "win32"
  });
}
